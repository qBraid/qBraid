"""
Unit tests for interfacing quantum programs

"""
import pytest

from qbraid._version import __version__
from qbraid.interface import random_circuit
from qbraid.interface.random.cirq_random import _cirq_random
from qbraid.interface.random.qasm3_random import _qasm3_random
from qbraid.interface.random.qiskit_random import _qiskit_random
from qbraid.programs.exceptions import PackageValueError


@pytest.mark.parametrize("num_qubits, seed, depth, max_operands", [(0, 2, 1, 2)])
def test_qasm3_random_with_zero_qubits(num_qubits, seed, depth, max_operands):
    """Test that _qasm3_random generates the correct QASM code when num_qubits is 0."""
    expected_output = f"""
// Generated by qBraid v{__version__}
OPENQASM 3.0;
include "stdgates.inc";
/*
    seed = {seed}
    num_qubits = {num_qubits}
    depth = {depth}
    max_operands = {max_operands}
*/
"""
    output = _qasm3_random(num_qubits=0, seed=2, depth=1, max_operands=2)
    assert output == expected_output
